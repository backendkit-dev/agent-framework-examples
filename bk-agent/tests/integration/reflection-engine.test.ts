describe('Persistencia en disco (failures.json)', () => {
  const testDir = path.join(os.tmpdir(), 'reflection-test-persistence');
  let engine: ReflectionEngine;

  beforeEach(async () => {
    try { await fs.promises.rm(testDir, { recursive: true }); } catch {}
    engine = new ReflectionEngine({ projectRoot: testDir, useGlobalDir: false });
    await engine.initialize();
  });

  afterAll(async () => {
    try { await fs.promises.rm(testDir, { recursive: true }); } catch {}
  });

  it('should persist incidentes de BootstrapHook al disco via ReflectionEngine', async () => {
    const hook = new BootstrapHook(engine);

    // Reportar 3 incidentes de bootstrap
    await hook.reportBootstrapFailure('No se encontró config.yaml', ['config.yaml']);
    await hook.reportManifestCorrupt('Error al parsear YAML', 'manifest.yaml');
    await hook.reportMemoryLoadFailure('memory/session.md', 'Archivo no encontrado');

    // Verificar que el archivo failures.json existe en disco
    const catalog = engine.getCatalog();
    const catalogPath = catalog.getCatalogPath();
    const content = await fs.promises.readFile(catalogPath, 'utf-8');
    const records = JSON.parse(content) as any[];

    expect(records.length).toBeGreaterThanOrEqual(3);

    // Verificar estructura correcta
    const bootstrapRecords = records.filter((r: any) => r.domain === 'bootstrap');
    expect(bootstrapRecords.length).toBeGreaterThanOrEqual(3);

    // Verificar tipos de fallo específicos
    const failureTypes = bootstrapRecords.map((r: any) => r.failureType);
    expect(failureTypes).toContain('missing_config_yaml');
    expect(failureTypes).toContain('manifest_corrupt');
    expect(failureTypes).toContain('memory_load_failure');

    // Verificar campos requeridos en cada registro
    for (const record of bootstrapRecords) {
      expect(record.id).toBeDefined();
      expect(record.domain).toBe('bootstrap');
      expect(record.failureType).toBeDefined();
      expect(record.severity).toBeDefined();
      expect(record.hallazgo).toBeDefined();
      expect(record.recomendacion).toBeDefined();
      expect(record.fecha).toBeDefined();
    }
  });

  it('should persist incidentes de todos los dominios y poder leerlos del archivo', async () => {
    const hook = new BootstrapHook(engine);

    // Reportar incidentes de diferentes tipos
    await hook.reportBootstrapFailure('No se encontró config.yaml', ['config.yaml']);
    await hook.reportManifestCorrupt('Error al parsear YAML', 'manifest.yaml');

    // También reportar incidente de auditoría via engine directo
    await engine.reportIncident({
      domain: 'audit',
      failureType: 'connection_leak',
      severity: 'high',
      dimension: 'seguridad',
      gate: 'qa-engineer',
      agenteResponsable: 'qa',
      hallazgo: 'QueryRunner sin release',
      recomendacion: 'Agregar finally block',
      archivos: ['connection.ts'],
      fecha: new Date().toISOString(),
    });

    // Leer el archivo directamente
    const catalog = engine.getCatalog();
    const catalogPath = catalog.getCatalogPath();
    const content = await fs.promises.readFile(catalogPath, 'utf-8');
    const records = JSON.parse(content) as any[];

    expect(records.length).toBe(3);

    // Verificar distribución por dominio
    const domains = records.map((r: any) => r.domain);
    expect(domains.filter((d: string) => d === 'bootstrap')).toHaveLength(2);
    expect(domains.filter((d: string) => d === 'audit')).toHaveLength(1);
  });

  it('should mantener integridad del archivo JSON en escrituras sucesivas', async () => {
    const hook = new BootstrapHook(engine);

    // Primera escritura
    await hook.reportBootstrapFailure('Error #1', ['file1.yaml']);
    let content = await fs.promises.readFile(engine.getCatalog().getCatalogPath(), 'utf-8');
    let records = JSON.parse(content);
    expect(records).toHaveLength(1);

    // Segunda escritura
    await hook.reportManifestCorrupt('Error #2', 'manifest.yaml');
    content = await fs.promises.readFile(engine.getCatalog().getCatalogPath(), 'utf-8');
    records = JSON.parse(content);
    expect(records).toHaveLength(2);

    // Tercera escritura
    await hook.reportMemoryLoadFailure('session.md', 'Error #3');
    content = await fs.promises.readFile(engine.getCatalog().getCatalogPath(), 'utf-8');
    records = JSON.parse(content);
    expect(records).toHaveLength(3);

    // Verificar que todos los registros se mantienen
    expect(records[0].hallazgo).toContain('Error #1');
    expect(records[1].hallazgo).toContain('Error #2');
    expect(records[2].hallazgo).toContain('Error #3');
  });
});

