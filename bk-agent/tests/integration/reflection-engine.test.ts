describe('ReflectionEngine', () => {
  const testDir = path.join(os.tmpdir(), 'reflection-test-engine');
  let engine: ReflectionEngine;

  beforeEach(async () => {
    try { await fs.promises.rm(testDir, { recursive: true }); } catch {}
    engine = new ReflectionEngine({
      projectRoot: testDir,
      useGlobalDir: false,
      config: { autoPromote: true },
    });
    await engine.initialize();
  });

  afterAll(async () => {
    try { await fs.promises.rm(testDir, { recursive: true }); } catch {}
  });

  it('should initialize with empty catalog', async () => {
    const stats = await engine.getStats();
    expect(stats.totalIncidents).toBe(0);
    expect(stats.enabled).toBe(true);
  });

  it('should report incident and detect patterns', async () => {
    // Reportar 3 incidentes del mismo tipo
    for (let i = 0; i < 3; i++) {
      await engine.reportIncident({
        domain: 'audit',
        failureType: 'connection_leak',
        severity: 'high',
        dimension: 'seguridad',
        gate: 'qa-engineer',
        agenteResponsable: 'qa',
        hallazgo: `QueryRunner sin release #${i}`,
        recomendacion: 'Agregar finally block',
        archivos: ['connection.ts'],
        fecha: new Date().toISOString(),
      });
    }

    // Ejecutar reflexión
    const { patterns, promotedRules } = await engine.reflect();

    // Debería detectar el patrón
    expect(patterns.length).toBeGreaterThanOrEqual(1);
    const leakPattern = patterns.find(p => p.failureType === 'connection_leak');
    expect(leakPattern).toBeDefined();
    expect(leakPattern!.count).toBe(3);

    // Debería promover automáticamente
    if (engine['config'].autoPromote) {
      expect(promotedRules.length).toBeGreaterThanOrEqual(1);
    }
  });

  it('should get stats after incidents', async () => {
    await engine.reportIncident({
      domain: 'audit', failureType: 'connection_leak', severity: 'high', dimension: 'seguridad',
      gate: 'qa', agenteResponsable: 'qa', hallazgo: 'test', recomendacion: 'fix',
      archivos: [], fecha: new Date().toISOString(),
    });
    await engine.reportIncident({
      domain: 'test', failureType: 'jest_timeout', severity: 'high', dimension: 'rendimiento',
      gate: 'jest', agenteResponsable: 'system', hallazgo: 'test', recomendacion: 'fix',
      archivos: [], fecha: new Date().toISOString(),
    });

    const stats = await engine.getStats();
    expect(stats.totalIncidents).toBe(2);
    expect(stats.countsByDomain.audit).toBe(1);
    expect(stats.countsByDomain.test).toBe(1);
  });

  it('should report incident and return patterns', async () => {
    const result = await engine.reportIncident({
      domain: 'audit', failureType: 'connection_leak', severity: 'high', dimension: 'seguridad',
      gate: 'qa', agenteResponsable: 'qa', hallazgo: 'test', recomendacion: 'fix',
      archivos: [], fecha: new Date().toISOString(),
    });

    expect(result.record.id).toMatch(/^fail_/);
    expect(result.record.domain).toBe('audit');
    expect(result.record.failureType).toBe('connection_leak');
  });
});

