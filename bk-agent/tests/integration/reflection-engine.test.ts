describe('Integración Bootstrap Loaders → ReflectionEngine', () => {
  const testDir = path.join(os.tmpdir(), 'reflection-test-loaders');
  let engine: ReflectionEngine;
  let hook: BootstrapHook;

  beforeEach(async () => {
    try { await fs.promises.rm(testDir, { recursive: true }); } catch {}
    engine = new ReflectionEngine({ projectRoot: testDir, useGlobalDir: false });
    await engine.initialize();
    hook = new BootstrapHook(engine);
  });

  afterAll(async () => {
    try { await fs.promises.rm(testDir, { recursive: true }); } catch {}
  });

  it('config-loader debe reportar missing config cuando el archivo no existe', async () => {
    // Cargar con hook — archivo inexistente
    const config = await loadConfig(path.join(testDir, '_no-existe_.yaml'), { hook });

    // Debe retornar null
    expect(config).toBeNull();

    // El engine debe tener 1 incidente de bootstrap
    const stats = await engine.getStats();
    expect(stats.totalIncidents).toBe(1);

    // Verificar que se persistió en disco
    const catalog = engine.getCatalog();
    const records = await catalog.findByDomain('bootstrap');
    expect(records.length).toBe(1);
    expect(records[0].failureType).toBe('missing_config_yaml');
    expect(records[0].archivos.some((f: string) => f.includes('_no-existe_'))).toBe(true);
  });

  it('config-loader debe reportar manifest corrupt cuando el YAML es inválido', async () => {
    // Crear directorio y un archivo YAML inválido
    await fs.promises.mkdir(testDir, { recursive: true });
    const badYamlPath = path.join(testDir, 'bad-config.yaml');
    await fs.promises.writeFile(badYamlPath, '{{{invalid: yaml: [[[', 'utf-8');

    const config = await loadConfig(badYamlPath, { hook });

    // Debe retornar null
    expect(config).toBeNull();

    // El engine debe tener 1 incidente de manifest corrupt
    const records = await engine.getCatalog().findByDomain('bootstrap');
    expect(records.length).toBe(1);

    // yaml.parse lanza error (no ENOENT) → reportManifestCorrupt → manifest_corrupt
    expect(records[0].failureType).toBe('manifest_corrupt');
  });

  it('detector debe reportar missing config cuando no existe .ai-assistant', async () => {
    // Detectar en un directorio sin .ai-assistant
    const result = await detectProjectFiles(testDir, { hook });

    // Debe reportar que no existe
    expect(result.exists).toBe(false);

    // El engine debe tener un incidente de bootstrap
    const records = await engine.getCatalog().findByDomain('bootstrap');
    expect(records.length).toBeGreaterThanOrEqual(1);

    // Verificar que se reportó como missing_config_yaml
    const configRecords = records.filter((r: any) =>
      r.failureType === 'missing_config_yaml' && r.hallazgo.includes(testDir)
    );
    expect(configRecords.length).toBeGreaterThanOrEqual(1);
  });

  it('loader con hook undefined no debe fallar', async () => {
    // Pasar hook undefined, debe funcionar sin error
    const config = await loadConfig(path.join(testDir, '_no-existe_.yaml'), { hook: undefined });
    expect(config).toBeNull();

    // No debe haber incidentes
    const stats = await engine.getStats();
    expect(stats.totalIncidents).toBe(0);
  });
});
