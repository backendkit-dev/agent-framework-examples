describe('PolicyPromoter', () => {
  const testDir = path.join(os.tmpdir(), 'reflection-test-promoter');
  const manifestPath = path.join(testDir, 'manifest.yaml');

  const promoter = new PolicyPromoter({ manifestPath });

  afterAll(async () => {
    try { await fs.promises.rm(testDir, { recursive: true }); } catch {}
  });

  beforeEach(async () => {
    try { await fs.promises.rm(testDir, { recursive: true }); } catch {}
    await fs.promises.mkdir(testDir, { recursive: true });
  });

  const samplePattern: DetectedPattern = {
    failureType: 'connection_leak',
    domain: 'audit',
    count: 3,
    firstSeen: '2026-01-01',
    lastSeen: '2026-01-03',
    severity: 'high',
    recordIds: ['1', '2', '3'],
    dominantDimension: 'seguridad',
    dominantGate: 'qa',
    recommendedAction: 'Agregar finally block con release()',
    promotedToPolicy: false,
  };

  it('should promote a pattern to policyRule', async () => {
    const rule = await promoter.promote(samplePattern);
    expect(rule.id).toMatch(/^POL-AUDI-\d+$/);
    expect(rule.autoGenerated).toBe(true);
    expect(rule.trigger.domain).toBe('audit');
    expect(rule.trigger.pattern).toBe('connection_leak');
    expect(rule.name).toContain('Connection Leak');
  });

  it('should persist rules across reads', async () => {
    await promoter.promote(samplePattern);
    const rules = await promoter.getExistingRules();
    expect(rules.length).toBeGreaterThanOrEqual(1);
    expect(rules[0].id).toMatch(/^POL-AUDI-\d+$/);
  });

  it('should generate sequential IDs', async () => {
    const rule1 = await promoter.promote(samplePattern);
    const rule2 = await promoter.promote({ ...samplePattern, failureType: 'security_vulnerability' });
    const num1 = parseInt(rule1.id.split('-')[2]);
    const num2 = parseInt(rule2.id.split('-')[2]);
    expect(num2).toBeGreaterThan(num1);
  });

  it('should remove a rule by ID', async () => {
    const rule = await promoter.promote(samplePattern);
    const removed = await promoter.removeRule(rule.id);
    expect(removed).toBe(true);

    const rules = await promoter.getExistingRules();
    expect(rules.find(r => r.id === rule.id)).toBeUndefined();
  });

  it('should update an existing rule', async () => {
    const rule = await promoter.promote(samplePattern);
    samplePattern.promotedToPolicy = true;

    const updatedRule = await promoter.promote({ ...samplePattern, count: 5 }, rule.id);
    expect(updatedRule.id).toBe(rule.id);

    const manifest = await promoter.readManifest();
    const found = manifest.policyRules.find((r: any) => r.id === rule.id);
    expect(found).toBeDefined();
  });

  it('should write valid YAML', async () => {
    await promoter.promote(samplePattern);
    const content = await fs.promises.readFile(manifestPath, 'utf-8');
    expect(content).toContain('policyRules:');
    expect(content).toContain('POL-AUDI');
    expect(content).toContain('autoGenerated: true');
  });
});

